    private void BtnActualizarClienteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnActualizarClienteActionPerformed
        // TODO add your handling code here:
        try {
            //OBTIENE LOS VALORES DE LOS JTEXTFIELD
            String nombre = JTNombre.getText();
            String id = JTIdCliente.getText();
            String clave = JTClave.getText();
            String direccion = JTDireccion.getText();
            
            Cliente actualizarCliente = new Cliente(nombre, id, clave, direccion);
            actualizarCliente.setId(JTIdCliente.getText());
            actualizarCliente.setNombre(JTNombre.getText());
            actualizarCliente.setClave(JTClave.getText());
            actualizarCliente.setDireccion(JTDireccion.getText());
            cl.ModificarDatos(actualizarCliente);
            JOptionPane.showMessageDialog(null, "Cliente actualizado");
            JTClave.setText("");
            JTIdCliente.setText("");
            JTDireccion.setText("");
            JTNombre.setText("");
            JTNombre.requestFocus();
            
        } catch (Exception e){
            javax.swing.JOptionPane.showMessageDialog(this,
                    "Error: " + e.getMessage(),
                    "Error",
                    javax.swing.JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_BtnActualizarClienteActionPerformed

