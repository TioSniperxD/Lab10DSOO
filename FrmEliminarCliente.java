    public void MostrarDatos(){
        try {
            //OBTIENE LOS VALORES DE LOS JTEXTFIELD
            String id = JTIdCliente.getText();

            //VALIDA QUE NO ESTE VACIO
            if (id.isEmpty()) {
                javax.swing.JOptionPane.showMessageDialog(this,
                        "Ingrese su ID",
                        "Error",
                        javax.swing.JOptionPane.ERROR_MESSAGE);
                return;
            }
            Cliente mostrarCliente = new Cliente(id);
            mostrarCliente.setId(JTIdCliente.getText());
            List<Cliente> listCl = cliente.MostrarDatos(mostrarCliente);
            modelo = (DefaultTableModel) JTDatos.getModel();
            Object[] ob = new Object[3];
            for (int i = 0; i < listCl.size(); i++) {
                ob[0] = listCl.get(i).getId();
                ob[1] = listCl.get(i).getNombre();
                ob[2] = listCl.get(i).getDireccion();
                modelo.addRow(ob);
            }
            JTDatos.setModel(modelo);
        } catch (Exception e) {
            javax.swing.JOptionPane.showMessageDialog(this,
                "Error: " + e.getMessage(),
                "Error",
                javax.swing.JOptionPane.ERROR_MESSAGE);
        }
    }
    private void btnAceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAceptarActionPerformed
        // TODO add your handling code here:
        try {
            String idCliente = JTIdCliente.getText();
            
            if (idCliente.isEmpty()) {
                javax.swing.JOptionPane.showMessageDialog(this,
                        "El campo es obligatorio",
                        "Error",
                        javax.swing.JOptionPane.ERROR_MESSAGE);
                return;
            }else {
                int pregunta = JOptionPane.showConfirmDialog(null, "Esta seguro de eliminar el cliente?");
                if (pregunta == 0) {
                    cliente.EliminarCliente(idCliente);
                    JTIdCliente.setText("");
                }
            }
        } catch (Exception ex) {
            javax.swing.JOptionPane.showMessageDialog(this,
                    "Error: " + ex.getMessage(),
                    "Error del Sistema",
                    javax.swing.JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnAceptarActionPerformed

