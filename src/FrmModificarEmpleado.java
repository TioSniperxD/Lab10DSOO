    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
            try {
            //OBTIENE LOS VALORES DE LOS JTEXTFIELD
            String nombre = JTNombre.getText();
            String id = JTId.getText();
            String clave = JTClave.getText();
            String direccion = JTDireccion.getText();
            
            Empleado actualizarCliente = new Empleado(nombre, id, direccion, clave);
            actualizarCliente.setId(JTId.getText());
            actualizarCliente.setNombre(JTNombre.getText());
            actualizarCliente.setClave(JTClave.getText());
            actualizarCliente.setDireccion(JTDireccion.getText());
            el.ModificarDatos(actualizarCliente);
            JOptionPane.showMessageDialog(null, "Empleado actualizado");
            JTClave.setText("");
            JTId.setText("");
            JTDireccion.setText("");
            JTNombre.setText("");
            JTNombre.requestFocus();
            
        } catch (Exception e){
            javax.swing.JOptionPane.showMessageDialog(this,
                    "Error: " + e.getMessage(),
                    "Error",
                    javax.swing.JOptionPane.ERROR_MESSAGE);
        }
      
    }//GEN-LAST:event_jButton1ActionPerformed

