    private void btnConfirmarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConfirmarActionPerformed
        try {
        String idCliente = txtIdCliente.getText();
        String idCuenta = txtIdCuenta.getText();
        String textoMonto = txtMonto.getText();

        if (idCuenta.isEmpty() || textoMonto.isEmpty()) {
            javax.swing.JOptionPane.showMessageDialog(this, "Complete Cuenta y Monto.");
            return;
        }

        double monto = Double.parseDouble(textoMonto);

        if (monto > 0) {
            LogicaCajero logica = new LogicaCajero();

            // Responsable: El EMPLEADO (Ventanilla)
            // Usamos 'this.empleado' que es la variable de esta clase
            
            CuentaCliente nuevoDeposito = new CuentaCliente(idCliente, idCuenta, monto);
            nuevoDeposito.setId_cliente(idCliente);
            nuevoDeposito.setId_cuenta(idCuenta);
            nuevoDeposito.setSaldo(monto);
            cu.Deposito(nuevoDeposito);
            JOptionPane.showMessageDialog(null, "Deposito registrado");
            
            Transaccion nuevaTransaccion = new Transaccion(idCliente, idCuenta, monto, "Dep칩sito");
            nuevaTransaccion.setIdCliente(idCliente);
            nuevaTransaccion.setIdCuenta(idCuenta);
            nuevaTransaccion.setMonto(monto);
            nuevaTransaccion.setTipo("Dep칩sito");
            tc.RegistrarTransaccion(nuevaTransaccion);

        } else {
            javax.swing.JOptionPane.showMessageDialog(this, "El monto debe ser positivo.");
        }
    } catch (NumberFormatException ex) {
        javax.swing.JOptionPane.showMessageDialog(this, "Monto inv치lido.");
    }
    }//GEN-LAST:event_btnConfirmarActionPerformed

