    public void ListarClientes(){
        List<Cliente> listCl = cliente.ListarClientes();
        modelo = (DefaultTableModel) JTListaClientes.getModel();
        Object[] ob = new Object[3];
        for (int i = 0; i < listCl.size(); i++) {
            ob[0] = listCl.get(i).getId();
            ob[1] = listCl.get(i).getNombre();
            ob[2] = listCl.get(i).getDireccion();
            modelo.addRow(ob);
        }
        JTListaClientes.setModel(modelo);
    }
